<?php 
    // ?Pre-increment
    $x = 10;  
    echo ++$x;
    echo '<br>';

    // ?Post-increment
    $y = 10;  
    echo $y++; // ? increment value y and print y value
    echo '<br>';
    echo $y; 
    echo '<br>';

    // ?Pre-decrement
    $a = 10;  
    echo --$a;
    echo '<br>'; 

    // ?Post-decrement
    $b = 10; 
    echo $b--; // ? decrement value b and print b value
    echo '<br>';
    echo $b; 
    echo '<br>';
?>